# Напишите программу, которая принимает на вход цифру, обозначающую день недели, и проверяет, является ли этот день выходным.

# Пример:
# - 6 -> да
# - 7 -> да
# - 1 -> нет
print('Введите день недели от 1 до 7: ')
a = int(input())

def holyday(x):
    if x == 6 or x == 7:
        return 'Ура! Выходной!'
    elif x > 0 and x < 6:
        return 'Иди работай!'
    else:
        return 'Введите число от 1 до 7!' 

print(holyday(a))